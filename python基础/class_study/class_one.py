class Student:
	# 初始值None
	name=None
	gander=None
	nationality=None
	naive_place=None
	age=None

# 创建一个对象
st1=Student()
st1.name='王硕'
st1.gander='男'
st1.nationality='中国'
st1.naive_place='山东省'
st1.age=31
# 打印属性

print(st1.name)
print(st1.gander)
print(st1.nationality)
print(st1.naive_place)
print(st1.age)

